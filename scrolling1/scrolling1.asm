BasicUpstart2(main)

    /*
        ZP: This is all the ZP we can use without corrupting BASIC although we *could*
            get around it by copying ZP somewhere else and then copy it back when we're done.
            The stack might not be a great option though because it's the same size as ZP!

            The addresses below are used as working memory to allow the blitting of character
            and colour data to the VIC II memory bank. It's currently assuming that the VIC
            is pointing at bank 0 
            $f7:$f8  00d8 : $d800 : Colour RAM Ptr
            $f9:$fa  0030 : $4000 : screen color data Ptr
            $fb:$fc  0028 : $3000 : screen character data Ptr
            $fd:$fe  0004 : $0400 : screen memory Ptr        
    */

// dimensions
.const MAP_WIDTH = 80
.const SCREEN_WIDTH = 40
.const SCREEN_HEIGHT = 25

// keyboard scan codes
.const SC_A = 10
.const SC_S = 13
.const SC_D = 18
.const SC_W = 9
.const SC_NONE = 64

// kernal routine locations
.const SCNKEY = $ff9f
.const GETIN  = $ffe4

*=$1000

// The scroll offset is a 16 bit offset into both the character and colour data and
// holds the starting point for blits into character and colour video RAM. To caluclate
// the offset we need to use the equation (ypos * MAP_WIDTH) + xpos.
scroll_offset_lo: .byte 0
scroll_offset_hi: .byte 0

    // resets the zero page locaitons we need for doing a blit
reset_zp:

    lda #<$d800
    sta $f7
    lda #>$d800
    sta $f8

	lda #<map_colour_data
    sta $f9
    lda #>map_colour_data
    sta $fa

    lda #<map_character_codes
    sta $fb
    lda #>map_character_codes
    sta $fc

    lda #<$0400
    sta $fd
    lda #>$0400
    sta $fe
    rts

    // performs a blit from the character and colour maps into video memory
draw_screen:
	// draw screen
    jsr reset_zp

    // apply xpos to the map character data ptr
    lda $fb
    clc
    adc scroll_offset_lo
    sta $fb
    bcc !+
    inc $fc
!:
    lda $fc
    clc // there's nothing we can do about the carry here so we can probably remove this
    adc scroll_offset_hi
    sta $fc

    // apply scroll pos to the map colour data ptr
    lda $f9
    clc
    adc scroll_offset_lo
    sta $f9
    bcc !+
    inc $fa
!:
    lda $fa
    clc // there's nothing we can do about the carry here so we can probably remove this
    adc scroll_offset_hi
    sta $fa

    // draw a row of the screen
	ldx #$00
l1: ldy #$00

l2:	lda ($fb),y
	sta ($fd),y
	lda ($f9),y
	sta ($f7),y
	iny
    cpy #SCREEN_WIDTH
	bne l2

    // add 40 to the screen ptr
    lda $fd
    clc
    adc #SCREEN_WIDTH
    sta $fd
    bcc !+
    inc $fe
!:

    // add 40 to the colour ram ptr
    lda $f7
    clc
    adc #SCREEN_WIDTH
    sta $f7
    bcc !+
    inc $f8
!:

    // add 80 to the map character data ptr
    lda $fb
    clc
    adc #MAP_WIDTH
    sta $fb
    bcc !+
    inc $fc
!:

    // add 80 to the map colour data ptr
    lda $f9
    clc
    adc #MAP_WIDTH
    sta $f9
    bcc !+
    inc $fa
!:

    // incrememnt the row counter and check for last row
    inx
    cpx #SCREEN_HEIGHT
    beq done_frame

    // draw the next line of the frame
    jmp l1

done_frame:
    rts

main:
	// set to 25 line extended color text mode and turn on the screen
	lda #$5b
	sta $d011

	// disable SHIFT-Commodore
	lda #$80
	sta $0291

	// set screen memory ($0400) and charset bitmap offset ($2000)
	lda #$18
	sta $d018

	// set border color
	lda #$0E
	sta $d020
	
	// set background color
	lda #$06
	sta $d021

	// set extended bg color 1
	lda #$01
	sta $d022

	// set extended bg color 2
	lda #$02
	sta $d023

	// set extended bg color 3
	lda #$03
	sta $d024

    // reset scroll offset
    lda #0
    sta scroll_offset_lo
    sta scroll_offset_hi

    jsr draw_screen

    sei         // Disable interrupts

    lda #$7f    // Disable CIA timers
    sta $dc0d
    sta $dd0d

    lda #250    // Set raster line to trigger interrupt (adjust as needed)
    sta $d012

    lda #<irq   // Load low byte of interrupt handler address
    sta $0314   // Store it in the interrupt vector low byte
    lda #>irq   // Load high byte of interrupt handler address
    sta $0315   // Store it in the interrupt vector high byte

    lda #$81
    sta $d01a

    // clear out CIA and VIC interrupts that are
    // already in progress
    lda $dc0d
    lda $dd0d
    asl $d019

    // Enable interrupts
    cli     

    // infinite loop
    jmp *

    // this is the start of the main loop. Here we wait for a keypress and respond
    // to commands to scroll in various directions
irq:
    // raster time
    //inc $d020

    // scan for a key down
    jsr SCNKEY
    lda 203
    cmp #SC_NONE
	bne handle_down
    jmp irq_done

    // there should be a key value in the accumulator

handle_down:
    // check for scroll down
    cmp #SC_S
    bne handle_up

    // TODO: Check bounds!

    // scroll down - note this will overflow after about 3 or 4 rows
    lda scroll_offset_lo
    clc
    adc #MAP_WIDTH
    sta scroll_offset_lo
    bcc !+
    inc scroll_offset_hi
!:
    jmp done

handle_up:
    // check for scroll up
    cmp #SC_W
    bne handle_right

    // TODO: Check bounds!

    // scroll up
    lda scroll_offset_lo
    sec
    sbc #MAP_WIDTH
    sta scroll_offset_lo
    bcs !+
    dec scroll_offset_hi
!:
    jmp done

handle_right:
    // check for scroll right
    cmp #SC_D
    bne handle_left

    // TODO: Check bounds!

    // scroll right
    lda scroll_offset_lo
    clc
    adc #1
    sta scroll_offset_lo
    bcc !+
    inc scroll_offset_hi
!:
    jmp done

handle_left:
    // check for scroll left
    cmp #SC_A
    bne done

    // TODO: Check bounds!

    // scroll left
    lda scroll_offset_lo
    sec
    sbc #1
    sta scroll_offset_lo
    bcs !+
    dec scroll_offset_hi
!:

done:
    // redraw the screen
    // TODO: Update so screen isn't redrawn when an invalid key is pushed
    jsr draw_screen

irq_done:
    // raster time
    //dec $d020

    // set irq handled flag and end
    asl $d019
    
    // restore the registers and return from the interrupt handler
    pla
    tay
    pla
    tax
    pla
    rti 

